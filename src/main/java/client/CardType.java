package client;

public class CardType {
	private String name;
	private int id;
	
	public CardType() {}
	
	public CardType(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}
